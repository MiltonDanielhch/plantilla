# 1. El Script de "Análisis de Dependencias" (Ghost Inspector)
# Con el tiempo, instalas librerías que luego dejas de usar. Este script escanea tu código buscando qué librerías importas y las compara con tu archivo de configuración (package.json o requirements.txt).

# Misión: Detectar "código muerto" y librerías fantasma que solo ocupan espacio.

